void Config_GPIO(){
	RCC->AHB1ENR |= (0x15 << 0);
	GPIOA->MODER |= 0xAAAAAAAA;
	GPIOB->MODER |= 0xAAAAAAAA;
	GPIOA->AFR[0] |= 0x22222222;
	GPIOB->AFR[0] |= 0x22222222;
}